/*
Some good-to-follow rules when naming varaibles are:
1. Use human-readable names like firstName.
2. Stay away from abbreviations or short names like a, b, c.
3. Make names maximally descriptive and concise. Examples of bad names are data and value.
Such names say nothing. It’s only okay to use them if the context of the code makes it exceptionally
obvious which data or value the variable is referencing.
4. Agree on terms within your team and in your own mind. If a site visitor is called a “user”
then we should name related variables currentUser or newUser instead of currentVisitor or
newManInTown.
*/
